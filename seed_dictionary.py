def seed():
    # 讀取現有字典
    if os.path.exists(FILE_PATH):
        with open(FILE_PATH, "r", encoding="utf-8") as f:
            try:
                data = json.load(f)
            except json.JSONDecodeError:
                data = []
    else:
        data = []

    existing_words = {str(e.get("單字", "")).strip() for e in data if isinstance(e, dict)}
    added = 0

    for w in SEED_WORDS:
        word = w["單字"]
        if word in existing_words:
            continue

        entry = {
            "language": "japanese",
            "單字": word,
            "讀音": w.get("讀音", ""),
            "中文": w.get("中文", ""),
            "英文": "",
            "詞性": w.get("詞性", ""),
            "分類": w.get("分類", []),
            "例句": [],
            "用法": "",
            "補充": "",
            "圖片": "",
            "左頁分割": [0.18, 0.42, 0.40],
            "加入時間": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        }
        data.append(entry)
        existing_words.add(word)
        added += 1

    with open(FILE_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"[seed] Done! Added {added} words. Total: {len(data)} words.")

if __name__ == "__main__":
    seed()
